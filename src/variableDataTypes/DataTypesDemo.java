package variableDataTypes;

public class DataTypesDemo {
    public static void main(String[] args) {
        //Primitive DataTYpes -  Integer
        byte b = 127; //1 byte
        System.out.println(b);

        short s = 32000; //2 Bytes
        System.out.println(s);

        int i = 350002343; //4  bytes
        System.out.println(i);

        long l = 654646631656566445L; //8 bytes
        System.out.println(l);

        //Floating TYpe
        float x = 12.5f; //4 Bytes
        System.out.println(x);

        double y= 12.78; //8 bytes
        System.out.println(y);

        //char types
        char ch = 'x'; //2 bytes
        System.out.println(ch);

        //boolean Type
        boolean bl = true; //1 bit--> true =1, false=0
        System.out.print(bl);
    }
}
